func fetchTrivia(factNumber: Int) -> String {
  let facts = [
    "문어의 심장은 세 개이며, 그중 두 개는 아가미로만 혈액을 보냅니다.",
    "꿀은 상하지 않는 음식으로, 고대 이집트 무덤에서도 식용 가능한 꿀이 발견되었습니다.",
    "금성의 하루는 금성의 1년보다 깁니다.",
    "상어는 뼈가 없고 전부 연골로 이루어져 있습니다.",
    "바나나는 식물학적으로 베리(berry)에 해당합니다.",
    "개미는 자기 몸무게의 약 50배를 들어 올릴 수 있습니다.",
    "사람의 위산은 금속을 부식시킬 수 있을 정도로 강합니다.",
    "지구에서 가장 큰 생물은 동물이 아니라 균류(버섯)입니다.",
    "우주 공간에서는 소리가 전달되지 않습니다.",
    "고래는 물고기가 아니라 포유류입니다.",
    "번개는 태양 표면보다 더 뜨겁습니다.",
    "인간의 뇌는 약 75%가 물입니다.",
    "펭귄은 남반구에만 서식합니다.",
    "나비는 발로 맛을 느낍니다.",
    "토마토는 요리학적으로는 채소지만, 식물학적으로는 과일입니다.",
    "사람은 잠을 자는 동안에도 냄새에 반응할 수 있습니다.",
    "달은 매년 약 3.8cm씩 지구에서 멀어지고 있습니다.",
    "문어와 오징어는 파란 피를 가지고 있습니다.",
    "인간의 지문은 쌍둥이라도 서로 다릅니다.",
    "코알라는 하루의 대부분을 잠으로 보냅니다."
  ]
  
  guard factNumber >= 0 && factNumber < facts.count else {
    return "#(\(factNumber)): 더 이상 준비된 트리비아가 없습니다."
  }
  
  return "#(\(factNumber)): " + facts[factNumber]
}
